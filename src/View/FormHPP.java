    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(txtHpp.getText().equals("")|| txtHpp.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "Total HPP Tidak Boleh Kosong");
        }else {
            int bahanBaku = Integer.parseInt(txtBahanBaku.getText());
            int bahanPenolong = Integer.parseInt(txtBahanPenolong.getText());
            int tkl = Integer.parseInt(txtTKL.getText());
            int total = bahanBaku + bahanPenolong + tkl;

            hd.Update(cbJenis.getSelectedItem().toString(), txtBahanBaku.getText(), txtBahanPenolong.getText(), txtTKL.getText(), txtHpp.getText(), hd.getId());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

