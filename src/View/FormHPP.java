    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        // TODO add your handling code here:
        if (txtBahanBaku.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Bahan Baku Kosong !!");
        } else if (txtBahanPenolong.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Bahan Penolong Kosong !!");
        } else if (txtTKL.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Tenaga Kerja Langsung Kosong !!");
        } else {

            int bahanBaku = Integer.parseInt(txtBahanBaku.getText());
            int bahanPenolong = Integer.parseInt(txtBahanPenolong.getText());
            int tkl = Integer.parseInt(txtTKL.getText());
            int total = bahanBaku + bahanPenolong + tkl;

            txtHpp.setText(String.valueOf(total));
            btnTotal.setEnabled(false);
            btnSave.setEnabled(true);
        }
    }//GEN-LAST:event_btnTotalActionPerformed

