    private void btnTampilHasilTarifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilHasilTarifActionPerformed
        // TODO add your handling code here:
        int rowTable = tblCD.getRowCount();
        int rowJenis = cbJenis.getItemCount();

        if (rowTable != rowJenis) {
            JOptionPane.showMessageDialog(null, "Data barang belum ditambahkan seluruhnya, Silahkan di Inpur Terlebih Dahulu!!");
        
        } else {

            ArrayList<tbKlasifikasi> list = new ArrayList<>();
            list = td.ListTarif();
            txtBiayaPenolong.setText(list.get(0).getBiayaPenolong());
            txtBiayaDepresiasiMesin.setText(list.get(0).getBiayaMesin());
            txtBiayaListrik.setText(list.get(0).getBiayaListrik());
            txtBiayaPemeliharaanMesin.setText(list.get(0).getBiayaMtnMesin());
            txtBiayaPemasaran.setText(list.get(0).getBiayaPemasaran());
            txtBiayaPemeliharaanBangunan.setText(list.get(0).getBiayaMtnBangunan());
            txtBiayaDepresiasiBangunan.setText(list.get(0).getBiayaBangunan());

            double totalPool1 = 0.0;
            double totalPool2 = 0.0;
            double totalPool3 = 0.0;
            double totalPool4 = 0.0;
            double totalPool5 = 0.0;

            double unit = Double.parseDouble(txtTotalUnitProduksi.getText());
            double penolong = Double.parseDouble(txtBiayaPenolong.getText());
            double depMesin = Double.parseDouble(txtBiayaDepresiasiMesin.getText());
            totalPool1 = penolong + depMesin;
            txtTotalPool1.setText(String.valueOf(totalPool1));
            txtPoolRate1.setText(String.valueOf(String.format("%.2f", totalPool1 / unit)));

            double listrik = Double.parseDouble(txtBiayaListrik.getText());

            totalPool2 = listrik;
            txtTotalPool2.setText(String.valueOf(totalPool2));
            txtPoolRate2.setText(String.valueOf(String.format("%.2f", listrik / Double.parseDouble(txtTotalKWH.getText()))));

            double mtnMesin = Double.parseDouble(txtBiayaPemeliharaanMesin.getText());
            totalPool3 = mtnMesin;
            txtTotalPool3.setText(String.valueOf(totalPool3));
            txtPoolRate3.setText(String.valueOf(String.format("%.2f", mtnMesin / Double.parseDouble(totalInspeksi1.getText()))));

            double pemasaran = Double.parseDouble(txtBiayaPemasaran.getText());

            totalPool4 = pemasaran;
            txtTotalPool4.setText(String.valueOf(totalPool4));
            txtPoolRate4.setText(String.valueOf(String.format("%.2f", pemasaran / unit)));

            double mtnBangunan = Double.parseDouble(txtBiayaPemeliharaanBangunan.getText());
            double depBangunan = Double.parseDouble(txtBiayaDepresiasiBangunan.getText());
            double luasArea = Double.parseDouble(txtLuasAreaTotal.getText());
            totalPool5 = mtnBangunan + depBangunan;
            txtTotalPool5.setText(String.valueOf(totalPool5));
            txtPoolRate5.setText(String.valueOf(String.format("%.2f", totalPool5 / luasArea)));

            btnTampilHasilTarif.setEnabled(false);
        }

    }//GEN-LAST:event_btnTampilHasilTarifActionPerformed

    private void cbJenisLemariItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbJenisLemariItemStateChanged
        // TODO add your handling code here:
        con = new Koneksi();
        try {
            st = con.connect().createStatement();
            res = st.executeQuery("SELECT *FROM tb_costdriver WHERE jenis_lemari='" + cbJenisLemari.getSelectedItem().toString() + "'");
            while (res.next()) {
                txtUnitProduk2.setText(res.getString("jumlah_unit"));
                //hitung
                double unit = Double.parseDouble(txtUnitProduk2.getText());
                txtBebanUnitProduk2.setText(String.valueOf(Double.parseDouble(txtPoolRate1.getText()) * unit));
                txtKwh2.setText(String.valueOf((Double.parseDouble(res.getString("total_kwh"))) * (Double.parseDouble(txtPoolRate2.getText()))));
                txtInspeksi2.setText(String.valueOf(Double.parseDouble(res.getString("total_inspeksi")) * Double.parseDouble(txtPoolRate3.getText())));
                txtProduk2.setText(String.valueOf(unit * Double.parseDouble(txtPoolRate4.getText())));
                txtLuasArea2.setText(String.valueOf(Double.parseDouble(res.getString("luas_area")) * Double.parseDouble(txtPoolRate5.getText())));

                GetHppAwal(cbJenisLemari.getSelectedItem().toString());

                double pool1 = Double.parseDouble(txtBebanUnitProduk2.getText());
                double pool2 = Double.parseDouble(txtKwh2.getText());
                double pool3 = Double.parseDouble(txtInspeksi2.getText());
                double pool4 = Double.parseDouble(txtProduk2.getText());
                double pool5 = Double.parseDouble(txtLuasArea2.getText());

                txtOverhead2.setText(String.valueOf(Math.round(pool1 + pool2 + pool3 + pool4 + pool5)));

                double tkl = Double.parseDouble(txtTKL2.getText());
                double bahanBaku = Double.parseDouble(txtBahanBaku2.getText());
                double overhead = Double.parseDouble(txtOverhead2.getText());
                txtTotalHpp2.setText(String.valueOf(Math.round(tkl + bahanBaku + overhead)));

                txtHppPerUnit2.setText(String.valueOf(Math.round(Double.parseDouble(txtTotalHpp2.getText()) / unit)));

            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_cbJenisLemariItemStateChanged

