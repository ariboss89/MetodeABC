    private void txtJmlhUnitKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJmlhUnitKeyTyped
        // TODO add your handling code here:
        char karakter = evt.getKeyChar();

        if (!(((karakter >= '0') && (karakter <= '9') || (karakter == KeyEvent.VK_BACK_SPACE) || (karakter == KeyEvent.VK_DELETE) || (karakter == KeyEvent.VK_ENTER)))) {
            getToolkit().beep();
            evt.consume();
        }

        if (karakter == KeyEvent.VK_SPACE) {
            getToolkit().beep();
            evt.consume();
        }

    }//GEN-LAST:event_txtJmlhUnitKeyTyped

