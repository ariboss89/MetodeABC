    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        rsCD = cd.ShowData();
        tbd.SetTabel(tblCD, rsCD, namaKolomCD, jmlKolomCD, lebarCD);

        rsHasil = td.ShowData();
        tbd.SetTabel(jTable2, rsHasil, namaKolom, jmlKolom, lebar);

        int totalData = tblCD.getRowCount();
        int jumlahUnit = 0;
        int totalArea = 0;
        double jumlahKWH = 0.0;
        double totalInspeksi = 0.0;

        for (int a = 0; a < totalData; a++) {

            jumlahUnit += Integer.parseInt(tblCD.getValueAt(a, 2).toString());
            jumlahKWH += Double.parseDouble(tblCD.getValueAt(a, 3).toString());
            totalArea += Integer.parseInt(tblCD.getValueAt(a, 5).toString());
            totalInspeksi += Double.parseDouble(tblCD.getValueAt(a, 4).toString());
        }

        txtTotalUnitProduksi.setText(String.valueOf(jumlahUnit));
        txtLuasAreaTotal.setText(String.valueOf(totalArea));
        txtTotalKWH.setText(String.valueOf(jumlahKWH));
        totalInspeksi1.setText(String.valueOf(totalInspeksi));

    }//GEN-LAST:event_formWindowActivated

