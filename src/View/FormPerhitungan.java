    private void GetHppAwal(String lemari) {
        con = new Koneksi();
        try {
            st = con.connect().createStatement();
            res = st.executeQuery("SELECT *FROM tb_hpp WHERE jenis='" + lemari + "'");
            while (res.next()) {
                int bahanBaku = (Integer.parseInt(res.getString("bahan_baku")));
                int tkl = (Integer.parseInt(res.getString("tkl")));

                int unit = Integer.parseInt(txtUnitProduk2.getText());

                txtTKL2.setText(String.valueOf(unit * tkl));
                txtBahanBaku2.setText(String.valueOf(unit * bahanBaku));

            }
        } catch (Exception e) {

        }
    }

